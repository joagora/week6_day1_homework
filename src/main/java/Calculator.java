public class Calculator {


    public int addValues(int value1, int value2){
        int result = value1 + value2;
        return result;
    }

    public int subtractValues(int value1, int value2){
        int result = value1 - value2;
        return result;
    }

    public int multiplyValues(int value1, int value2){
        int result = value1 * value2;
        return result;
    }

    public double divideValues(double value1, double value2){
        double result = value1 / value2;
        return result;
    }
}
